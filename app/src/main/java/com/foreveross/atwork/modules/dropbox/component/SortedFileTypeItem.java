public class SortedFileTypeItem extends LinearLayout {

    private Context mContext;

    private TextView mTypeName;

    private ImageView mTypeIcon;

    public SortedFileTypeItem(Context context) {
        super(context);
        mContext = context;
        initViews();
    }

    private void initViews() {
        LayoutInflater inflater = (LayoutInflater)mContext.getSystemService(Context.LAYOUT_INFLATER_SERVICE);
        View view = inflater.inflate(R.layout.item_file_type, this);
        mTypeIcon = (ImageView)view.findViewById(R.id.type_icon);
        mTypeName = (TextView)view.findViewById(R.id.type_name);
    }

    public void setTypeName(String name) {
        mTypeName.setText(name);
    }

    public void setTypeIcon(int resId) {
        mTypeIcon.setImageResource(resId);
    }
}
