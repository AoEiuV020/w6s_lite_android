public class DropboxListFooterView extends LinearLayout {

    private Context mContext;

    private TextView mTotalSize;

    public DropboxListFooterView(Context context) {
        super(context);
        mContext = context;
        initView(context);
    }

    private void initView(Context context) {
        LayoutInflater layoutInflater = (LayoutInflater)context.getSystemService(Context.LAYOUT_INFLATER_SERVICE);
        View view = layoutInflater.inflate(R.layout.view_dropbox_footer, this);
        mTotalSize = (TextView) view.findViewById(R.id.total_size);
    }

    public void setUsedText(String usedText) {
        mTotalSize.setVisibility(View.VISIBLE);
        mTotalSize.setText(usedText);
    }

}
